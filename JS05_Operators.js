// 연산자
// ( + - * / % )

/*
1. 나머지(%)를 어디에 쓸까?
 홀수 : x % 2 = 1
 짝수 : y % 2 = 0

 어떤 값이 들어와도 5를 넘기면 안된다.
 x % 5 = 0 ~ 4 사이의 값만 반환
*/

/* 거듭제곱
const num = 2 ** 3;     // 2^3

console.log(num); 	// 8
*/

/* 우선 순위

    *,/ > +, -

*/

/* 연산자 줄여서 쓰기*/

let num = 10;
num = num + 5;
num += 5;

console.log(num)

// 나머지 연산자도 같은 방법으로 사용

/* 증가 연산자, 감소 연산자 */

let num = 10;
let result = num++; // 10

let result = ++num; // 11

console.log(result);

